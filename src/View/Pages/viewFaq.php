    <h3>L’exposition</h3>

    <p><strong>C'est quoi l'exposition? </strong></p>
    <p>L'exposition c'est le fait de laisser entrer plus ou moins de lumières dans le capteur de l'appareil.
        Le but étant d'avoir la meilleure exposition possible afin de faire des photos le plus proche de
        réalité. (Attention, certains font exprès de faire des photos sous ou sur exposée afin de donner
        un côté artistique à celle-ci). Un appareil peut laisser entrer trop de lumière, on dit alors que
        la photo est sur-exposée. Un appareil peut laisser entrer peu de lumière, on dit alors que la photo
        est sous-exposée. Un appareil peut également laisser entre la lumière qu'il faut afin que cette
        photo soit normalement exposée. L'exposition est influencée par 3 paramètres qui sont :
        la sensibilité ISO (sensibilité du capteur), l'ouverture du diaphragme ( diamètre d’ouverture)
        et la vitesse d’obturation (temps d’ouverture).
    </p>

    <h3>L’ouverture</h3>

    <p><strong>C'est quoi l'ouverture?</strong></p>
    <p> L'ouverture du diaphragme permet de laisser plus ou moins entrer de lumière. En effet, plus
        l'ouverture est grande, plus elle laisse entrer de lumière. (Plus le chiffre est donc petit,
        du type : f/1.8) Alors que plus l'ouverture est petite, moins elle laisse entrer de lumière.
        (Plus le chiffre est donc grand, du type : f/22). A savoir que cela joue également sur la netteté
        de l'arrière plan. Si l'ouverture est grande, alors votre sujet risque d'être précis et l'arrière
        plan flou, et inversement, si l'ouverture est petite alors la photo entière risque d'être nette.
    </p>

    <h3>La vitesse</h3>

    <p><strong>C'est quoi la vitesse?</strong></p>
    <p> La vitesse d’obturation permet de laisser plus ou moins entrer de lumière. En effet, plus la
        vitesse est petite, plus elle laisse entrer de lumière. (Elle a le temps). Alors que plus la
        vitesse est grande, moins elle laisse entrer de lumière. (Elle a peu de temps). A savoir que cela
        joue également sur la netteté des photos. Si le sujet est en mouvement, il va falloir avoir
        une vitesse assez grande afin que celui-ci ne soit pas flou. Par contre, si le sujet est
        immobile, vous pouvez utiliser une vitesse lente.
    </p>

    <h3>La sensibilité iso</h3>

    <p><strong>C'est quoi la sensibilité iso?</strong></p>
    <p> La sensibilité iso permet de laisser plus ou moins entrer de lumière.
        En effet, plus la sensibilité est élevée, plus elle laisse entrer entre
        de lumière. Alors plus la sensibilité est basse, moins elle laisse entrer de
        lumière. A savoir que cela joue également sur le grain de l’image. Si vous utilisez
        une sensibilité élevée, alors vous risquez de voir apparaître du grain sur celle-ci.
        Il est donc préférable de monter ce paramètre en dernier.
    </p>

    <h3>Sur-exposition</h3>

    <p><strong>C'est quoi la sur-exposition?</strong></p>
    <p> La surexposition survient lorsque trop de lumière entre dans le capteur de l’appareil.
        Celle-ci paraît donc trop lumineuse, et dans certains cas on peut dire qu’elle est cramée
        (complètement blanche). Certains utilisent la sur-exposition pour donner un aspect artistique
        à leurs photos. Attention, il est difficile voir impossible dans certains cas de récupérer une
        photo sur-exposée en post traitement. Donc pensez y lors de la prise de vue.
    </p>

    <h3>Sous-exposition</h3>

    <p><strong>C'est quoi la sous-exposition?</strong></p>
    <p>La sous-exposition survient lorsque pas assez de lumière n’entre dans le capteur de l’appareil.
        Celle-ci paraît donc trop sombre, et dans certains cas elle peut être complètement noire. Certains
        utilisent la sous-exposition pour donner un aspect artistique à leurs photos. Attention, il est
        difficile voir impossible dans certains cas de récupérer une photo sous-exposée en post traitement.
        Donc pensez y lors de la prise de vue.
    </p>

