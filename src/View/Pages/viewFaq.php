    <h3>Compact</h3>

    <p><strong>C'est quoi les compacts ?</strong></p>
    <p> Les appareils photos compactes sont utilisés en général dans un cadre amateur. Le budget
        pour ce genre d’appareil est assez bas. Il a pour avantages d’avoir un prix très abordable,
        d’être simple d’utilisation et d’être petit et facilement transportable. Il a pour
        inconvénient un capteur de petite taille, il est peu réactif, des mauvais rendus en basse
        lumière et les objectifs ne sont pas changeables.
    </p>

    <h3>Bridge</h3>

    <p><strong>C'est quoi les bridges ?</strong></p>
    <p> Les appareils photos bridges sont utilisés en général dans un cadre amateur.
        Le budget pour ce genre d’appareil est plus élevé que les compactes mais reste plutôt bas.
        Il a pour avantages d’avoir un zoom plus intéressant que sur les compactes, d’être simple
        d’utilisation et d’avoir un viseur. Il a les mêmes inconvénient que les compactes et est moins
        facilement transportable.
    </p>

    <h3>Reflex</h3>

    <p><strong>C'est quoi les reflex ?</strong></p>
    <p> Les appareils photos reflex sont utilisés en général dans un cadre amateur, semi-pro et pro.
        Le budget pour ce genre d’appareil est élevé, surtout dans un cadre pro. Il a pour avantages
        des objectifs interchangeables, une bonne voir très bonne réactivité, des bons rendus en basse
        lumière, un viseur. Il a pour inconvénient un prix élevé, il est encombrant, voir très
        encombrant, il peut être compliqué d’utilisation surtout sur les appareils pro.
    </p>

    <h3>Hybrides</h3>

    <p><strong>C'est quoi les hybrides ?</strong></p>
    <p> Les appareils photos hybrides sont utilisés en général dans un cadre amateur, semi-pro et pro.
        Le budget pour ce genre d’appareil est élevé, surtout dans un cadre pro. Il a les mêmes avantages
        que les reflex mais et également plus compact. Il a pour inconvénient un prix élevé, il peut être
        compliqué d’utilisation surtout sur les appareils pro.
    </p>

    <h3>La composition</h3>

    <p><strong>C'est quoi la composition ?</strong></p>
    <p> La composition est l’art d’arranger les différents éléments qui entrent dans le cadre
        photographié, de façon plaisante, agréable à regarder. Cette organisation doit être simple
        pour être efficace, et attirer très vite le regard du spectateur. C’est un des secrets des
        meilleurs photographes. De nombreux éléments peuvent entrer en compte dans celle-ci tel que
        la règle des tiers, les points de vues, la plongée/contre plongée ou encore les différents
        plans. (Premier plan, arrière plan).
    </p>

    <h3>La règle des tiers</h3>

    <p><strong>C'est quoi la règle des tiers ?</strong></p>
    <p> La règle des tiers propose que l'image soit imaginée comme divisée en neuf parties égales
        par deux lignes horizontales équidistantes et deux lignes verticales équidistantes
        (les lignes de tiers), les éléments importants de la composition devant être placés le
        long de ces lignes ou de leurs intersections (points forts).
    </p>

    <h3>Les points de vues</h3>

    <p><strong>C'est quoi les points de vues ?</strong></p>
    <p> Le point de vue est la position de votre appareil photo (et la vôtre) par rapport à votre sujet.
        Le choix de cette position est capital car contrairement au dessin ou à la peinture où on agence
        les éléments à notre guise dans le cadre, en photo les différents éléments, objets et plans sont
        bien réels et nous sont souvent imposés.
    </p>

    <h3>La plongée</h3>

    <p><strong>C'est quoi la plongée ?</strong></p>
    <p> La plongée, c’est une photographie d'un sujet se trouvant en dessous de l'appareil photo.
        En d'autre termes, c'est une vue par le dessus. C’est une manière un peu plus artistique de
        composer sa photographie.
    </p>

    <h3>La contre plongée</h3>

    <p><strong>C'est quoi la contre plongée ?</strong></p>
    <p> La contre-plongée, c’est une photographie d'un sujet se trouvant au dessus de l'appareil
        photo. En d'autre termes, c'est une vue par le dessous. C’est une manière un peu plus
        artistique de composer sa photographie.
    </p>

    <h3>Premier plan</h3>

    <p><strong>C'est quoi le premier plan ?</strong></p>
    <p>le premier plan désigne le ou les objet(s) situé(s) à l’avant de la photo. Il s’agit souvent
        du sujet. Il peut être informatif, artistique ou encore directif.
    </p>

    <h3>Arrière plan</h3>

    <p><strong>C'est quoi l'arrière plan ?</strong></p>
    <p> le second plan ou arrière plan désigne ce qui se trouve derrière le sujet ou l’objet en
        premier plan. Il peut être l'élément principal de la photo ou encore une sorte de toile
        de fond pour mettre en valeur le sujet.
    </p>

    <h3>Plan large</h3>

    <p><strong>C'est quoi le plan large ?</strong></p>
    <p> le plan large ou plan d'ensemble englobe à la fois le sujet et son environnement.
        Les deux ont la même importance sur l’image.
    </p>

    <h3>Plan moyen</h3>

    <p><strong>C'est quoi le plan moyen ?</strong></p>
    <p> Le plan moyen se resserre sur le sujet, il prend le dessus sur son environnement
        même si l’environnement est encore bien présent.
    </p>

    <h3>Plan américain</h3>

    <p><strong>C'est quoi le plan américain ?</strong></p>
    <p> Le plan américain, très utilisé au cinéma, consiste à couper le sujet à mi-cuisse.
        Le sujet est isolé et le décor devient secondaire.
    </p>

    <h3>Plan serré</h3>

    <p><strong>C'est quoi le plan serré ?</strong></p>
    <p> Le plan serré ou plan rapproché consiste à couper le sujet au-dessus de
        la taille pour le mettre bien en évidence.
    </p>

    <h3>Gros plan</h3>

    <p><strong>C'est quoi le gros plan ?</strong></p>
    <p> Le gros plan met en avant une partie du corps du sujet. Son visage,
        son expression ou un autre détail occupe tout le cadre.
    </p>

